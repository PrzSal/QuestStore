package com.codecool.dream_is_green.model;

public class Level {

    private String levelName;
    private Integer expRequired;

    public Level(String levelName, Integer expRequired){

        this.levelName;
        this.expRequired;
    }

    public String getLevelName() {
        return levelName;
    }

    public Integer getExpRequired() {
        return expRequired;
    }


}
